<template>
    <div>
        <p v-show="isError">認証に失敗しました。</p>
        <form @submit.prevent="login">
            <h1>ログイン</h1>
            ユーザー名: <input type="name" v-model="name">
            メールアドレス: <input type="email" v-model="email">
            パスワード: <input type="password" v-model="password">
            もっかい: <input type="password" v-model="password_confirmation">
            <span v-model="isLogin">{{ isLogin }}</span>

            <button type="submit" class="btn btn-primary">ログイン</button>
        </form>
    </div>
</template>

<script>
export default {
    data () {
        return {
            isError: false,
            name: '',
            email: '',
            password: '',
            password_confirmation: '',

            isLogin: state.isLogin,
        }
    },
    methods: {
        login() {
            axios.post('/api/register', {
                name: this.name,
                email: this.email,
                password: this.password,
                password_confirmation: this.password_confirmation
            }).then(res => {
                const token = res.data.access_token;
                console.log('token : ' + token);
                axios.defaults.headers.common['Authorization'] = 'Bearer ' + token;
                state.isLogin = true;
                this.isLogin = true;
                // this.$router.push({path: '/login'});
            }).catch(error => {
                state.isLogin = false;
                this.isLogin = false;
                this.isError = true;
            });
        }
    }
}
</script>

